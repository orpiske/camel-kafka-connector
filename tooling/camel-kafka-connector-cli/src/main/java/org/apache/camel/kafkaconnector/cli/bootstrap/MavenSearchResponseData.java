package org.apache.camel.kafkaconnector.cli.bootstrap;

import java.util.ArrayList;
import java.util.List;

public class MavenSearchResponseData {
    private int numFound;
    private int start;

    private List<MavenSearchResponseDoc> docs = new ArrayList<>();

    public int getNumFound() {
        return numFound;
    }

    public void setNumFound(int numFound) {
        this.numFound = numFound;
    }

    public int getStart() {
        return start;
    }

    public void setStart(int start) {
        this.start = start;
    }

    public List<MavenSearchResponseDoc> getDocs() {
        return docs;
    }

    public void setDocs(List<MavenSearchResponseDoc> docs) {
        this.docs = docs;
    }
}
